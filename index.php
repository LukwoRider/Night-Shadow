        <h2>Les gaz à effet de serre prédominants</h2>
        <p>Certains gaz à effet de serre sont naturellement présents dans l'atmosphère, tels que la vapeur d'eau et le dioxyde de carbone. Bien que la vapeur d'eau, y compris les nuages, soit l'élément qui contribue le plus à l'effet de serre naturel, l'augmentation de cet effet depuis la révolution industrielle du XIXe siècle est attribuable aux émissions d'autres gaz à effet de serre résultant de notre activité humaine :</p>
        <br>
        <p>
        <ol>
            <li>Dioxyde de carbone (CO2) : Responsable de deux tiers de l'augmentation de l'effet de serre liée aux activités humaines, telles que la combustion de gaz et de pétrole, la déforestation et les émissions des cimenteries. L'évaluation de l'effet des autres gaz à effet de serre est généralement exprimée en équivalent CO2 (eq. CO2). Les émissions actuelles de CO2 continueront à influencer les concentrations atmosphériques et la température mondiale pendant plusieurs décennies, en raison de sa durée de vie atmosphérique dépassant les cent ans.</li>
            <li>Méthane (CH4) : Principalement généré par les élevages de ruminants, les rizières inondées, les décharges d'ordures et les activités pétrolières et gazières. Le méthane a une durée de vie atmosphérique d'environ 12 ans.</li>
            <li>Protoxyde d'azote (N2O) : Provenant des engrais azotés et de certains procédés chimiques, le N2O a une durée de vie d'environ 120 ans.</li>
            <li>Hexafluorure de soufre (SF6) : Possédant une durée de vie atmosphérique extrêmement longue, soit 50 000 ans, le SF6 est émis dans l'atmosphère.</li>
        </ol></p>
