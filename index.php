        <h2>Pourquoi la Terre se réchauffe-t-elle ?</h2>
        <p>En raison de l'effet de serre.</p>
        <br>
        <p>La Terre est constamment alimentée en énergie par le soleil. La fraction de cette énergie qui n'est pas renvoyée par l'atmosphère, y compris les nuages, ou la surface terrestre, est absorbée par cette dernière, entraînant son réchauffement. En retour, les surfaces et l'atmosphère émettent un rayonnement infrarouge, plus intense lorsque les surfaces sont chaudes. Certains gaz et les nuages absorbent une partie de ce rayonnement, créant ainsi l'effet de serre. L'autre partie est émise dans l'univers, et la température de la Terre s'ajuste pour atteindre un équilibre entre l'énergie solaire constamment absorbée et celle réémise sous forme de rayonnement infrarouge.</p>
        <br>
        <p>L'activité humaine a entraîné une augmentation des gaz à effet de serre, piégeant une partie de ce rayonnement et induisant ainsi une augmentation de la température des surfaces jusqu'à atteindre un nouvel équilibre. Ceci constitue la principale cause du réchauffement climatique observé au cours des dernières décennies.</p>
